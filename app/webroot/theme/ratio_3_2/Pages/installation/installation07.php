<h3>任意のウィジェット</h3>

<p>以下のウィジェットエリアは、利用しなくても問題ありません。「blank」データではすべてのウィジェットが非表示となっています。</p>

<h4>ホーム::下段</h4>

<h4>ホーム以外::下段</h4>

<p>下段では、背景に色が付き、PCでは幅広く表示されます。地図などを強調したいときにおすすめですが、スマートフォンではかなり下の方に表示されます。</p>

<h4>ホーム以外::本文上</h4>

<p>重要なお知らせがあるときに、ここにテキストウィジェットを追加するのがおすすめです。</p>

<h3>フィードリーダーウィジェットについて</h3>

<p>このテーマでは、「フィードリーダー」プラグインに対応したウィジェットが登録されています。</p>

<p>3つの「フィード設定」が登録されており、<strong>「1」はTwitter、「2」はInstagram、「3」は外部フィード</strong>を読み込む前提となっています。<br />
利用する場合は、表示したいエリアの該当ウィジェットの「利用する」のチェックを外してください。</p>

<p>初期状態では、baserCMSとウェビングスタジオのアカウントが読み込まれています。変更する方法は「カスタマイズ」のページを参照ください。</p>

