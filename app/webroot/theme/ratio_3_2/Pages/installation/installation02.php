<h3>基本項目</h3>

<p>基本項目の内容は、baserCMSの内容になっています。自分のウェブサイトにあった内容に書き換えてください。</p>

<h4>WEBサイト名</h4>

<p>先ほど決めた「正式なウェブサイト名」を入力してください。</p>

<h4>WEBサイトタイトル</h4>

<p>先ほど決めた「ウェブサイト名」を入力してください。これは、トップページ以外のタイトルの最後にも表示されます。</p>

<h4>サイト基本キーワード</h4>

<p>空欄で構いません。</p>

<h4>サイト基本説明文</h4>

<p>先ほど決めた、ウェブサイトの説明文を入力してください。</p>

<h3>オプション</h3>

<p>「オプション」ボタンをクリックして細かい設定を開き、以下の項目を編集してください。</p>

<h4>GoogleMaps住所</h4>

<p>GoogleMapsの地図を出すことができます。店舗がある場合は、できるだけ正確な住所を書いてください。郵便番号は不要です。</p>

<h4>モバイル・スマートフォン</h4>

<p><strong>いずれも、「対応する」のチェックを外してください。</strong><br />
このテーマは、端末の画面サイズによって最適な表示を提供します（レスポンシブウェブデザイン）。このため、モバイル用のテンプレートは利用していません。</p>

<p>すべて入力したら、画面下の「<strong>保存</strong>」ボタンを押してください。<br />
なお、baserCMSは「保存」ボタンはすべて、画面の下にあります。押し忘れないようにしましょう。</p>