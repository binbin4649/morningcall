<p>このテーマでは、三種類の体裁のフィードを表示することができます。</p>

<p class="h4">Twitter用</p>

<p>つぶやき・つぶやいたアカウントID・投稿日時が表示されます。</p>

<p class="h4">Instagram用</p>

<p>写真と文章が表示されます。</p>

<h2 class="h4">その他のフィード用</h2>

<p>タイトル・概要・投稿日が表示されます。</p>

<h2>Twitter・InstagramのフィードURLを取得する</h2>

<p>TwitterとInstagramのRSSフィードを取得するには、外部サービスを利用する必要があります。</p>

<p>下記のサービスで、アカウント用のRSSフィードを作成し、URLを控えてください。</p>

<p>Queryfeed<br />
<a href="https://queryfeed.net/">https://queryfeed.net/</a></p>

<p>RSS.Stagram!!!<br />
<a href="http://rss.stagram.tk/">http://rss.stagram.tk/</a></p>

<h2>フィードURLを登録する</h2>

<p>管理画面の「フィード管理 &gt; フィード設定一覧」から、各サービスのフィード設定を選択してください。</p>

<p>TwitterにはbaserCMS、Instagramにはウェビングスタジオのフィードが登録されています。<br />
これを、ご利用のフィードURLに書き換えてください。</p>

<h2>フィードウィジェットを表示する</h2>

<p>管理画面の「ウィジェットエリア管理 &gt; ウィジェットエリア一覧」へ移動し、表示したい箇所のフィードウィジェットの「利用する」にチェックを入れてください。</p>

<div class="alert alert-danger">「Queryfeed」「RSS.Stagram!!!」は外部サービスです。予告なくサービスが終了する可能性があり、その場合はフィードが正常に表示されなくなりますので、事前にご了承ください。</div>